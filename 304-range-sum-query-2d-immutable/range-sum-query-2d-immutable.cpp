class NumMatrix {
public:
    vector<vector<int>> pref;
    NumMatrix(vector<vector<int>>& arr) {

        int n=arr.size();
        int m=arr[0].size();

        pref.assign(n+1, vector<int>(m+1, 0));

        for(int i = 1; i <= n; i++) {
            for(int j = 1; j <= m; j++) {
                pref[i][j] = arr[i-1][j-1]
                           + pref[i-1][j]
                           + pref[i][j-1]
                           - pref[i-1][j-1];
                }
            }
                
        
    }
    
    int sumRegion(int u, int l, int d, int r) {

        return pref[d+1][r+1]
             - pref[u][r+1]
             - pref[d+1][l]
             + pref[u][l];
        
    }
};

